function validate() {
  $("#result").text("");
  var email = $("#email").val();
  if (validateEmail(email)) {
    $("#result").text(email + " is valid :)");
    $("#result").css("color", "green");
    $.post(customerEmail.json, JSON.stringify({email: email}), onSuccess)
    return true;
  } 
  $("#result").text(email + " is not valid :(");
  $("#result").css("color", "red");
  return false;
}

